package Data_Types;

public class RelationalOperators {

	public static void main (String[]args) {
		int a=2;
		int b=3;
		int c=2;
		
		
		System.out.println(a==b); //false
		
		System.out.println(a=c);//true
		
		System.out.println(a!=c);//false
		
		System.out.println(a>b); //false
		System.out.println(a<b);//true
		
		System.out.println(b>=a);
		
		
	}
}
