package Iterative_statements;

public class ForEachl {
	
	public static void main (String[]args) {
		int arr[]= {1,2,3,4,6,78,9};
		
		
		for(int s:arr) {
			System.out.println(s);
			
		}
		
		String st[]= {"Apple","Orange","Toy"};
		
		for(String h:st) {
			System.out.println(h);
		}
		
		
		
	}

}
